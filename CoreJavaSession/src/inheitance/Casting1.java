package inheitance;

public class Casting1 {
public static void main(String[] args) {
		
		//datatypes
		// when u convert higher datatype to lower explicit casting is required
		int a=67;
		char ch=(char) a;
		System.out.println(ch);
		
		// when u convert lower datatype to higher implicit casting is required
		char ch1='Z';
		int x=ch1;
		System.out.println(x);
		

	}


}
