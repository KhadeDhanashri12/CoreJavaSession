package loopingstatement;

public class EvenNo {

	public static void main(String[] args) {
		 int n = 229;
         
	        //print all even numbers <=n 
	        int i=121;
	        while (i<=n) {
	            //check if i is exactly divisible by 2
	            if (i%2==0) {
	                System.out.print(i+"  ");
	            }
	            i++;
	        }

	}

}
