package basicsofjava;

public class CharCaseConversion {

	public static void main(String[] args) {
		//small to capital -32
				char ch='a';
				int i=ch-32;
				System.out.println((char)i);
				
				//capital to small +32
				char c='B';
				int x=c+32;
				System.out.println((char)x);
				
	}

}
