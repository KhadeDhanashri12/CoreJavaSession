package introduction_variables_datatype;

public class Variables_Datatypes {

	public static void main(String[] args) {
		 // Primitive Data Types
		
		 // Primitive Data Types
		
		 int a = 10;   // int type variable declaration
		 System.out.println(a);
		 
		 float b = 10.0f; // float type variable declaration
		 System.out.println(b);
		 
		 long c = 1234567; // long type variable declaration
		 System.out.println(c);
		 
		 double d = 34244.54; // double type variable declaration
		 System.out.println(d);
		 
		 char e = 'o'; // char type variable declaration
		 System.out.println(e);
		 
		 boolean f = false; // boolean type variable declaration
		 System.out.println(f);
		 
		 byte g = 12; // byte type variable declaration
		 System.out.println(g);
		 
		 short i = 22; // short type variable declaration
		 System.out.println(i);
		 
		 // Non-Primitive Data Types
		 
		 String j = "TQDP"; // String type variable declaration
		 System.out.println(j);
		 
		 int arr[]=new int[3]; // int type array declaration
		 System.out.println(arr);
		 
		 
		 
		 
		 
		 
		
	}

}
